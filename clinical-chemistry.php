                                <p>Clinical chemistry is the area of chemistry that is generally concerned with the analysis of bodily fluids for diagnostic and therapeutic purposes. It is an applied form of biochemistry. The discipline originated in the late 19th century with the use of simple chemical reaction tests for various components of blood and urine. In the many decades since, other techniques have been applied as science and technology have advanced, including the use and measurement of enzyme activities, spectrophotometry, electrophoresis, and immunoassay. There are now many blood tests and clinical urine tests with extensive diagnostic capabilities.</p>
