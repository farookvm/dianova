                            <h2>Histopathology</h2>
                            <div class="text">
                                <p>Histopathology is the diagnosis and study of diseases of the tissues and involves examining tissues and/or cells under a microscope. Histopathologists are responsible for making tissue diagnoses and helping clinicians manage a patient’s care. Histopathologists are doctors who work closely with other clinical specialties. They can reach a diagnosis by examining a small piece of tissue from the skin, liver, kidney, or other organs. This is called a biopsy. They examine the tissue carefully under a microscope, looking for changes in cells that might explain what is causing a patient’s illness. Around 20 million histopathology slides are examined each year.</p>
                                <p class="mb-30">Histopathologists provide a diagnostic service for cancer; they handle the cells and tissues removed from suspicious ‘lumps and bumps’, identify the nature of the abnormality and, if malignant, provide information to the clinician about the type of cancer, its grade and, for some cancers, its responsiveness to certain treatments. With the help of sophisticated imaging techniques, biopsy tissue can now be obtained from previously inaccessible sites such as the pancreas or retroperitoneum (behind the peritoneum, the membrane lining the abdominal cavity). The tissue is then processed, usually overnight, before being examined under a microscope.  In certain limited circumstances using special techniques, the specimen can be examined immediately. With rapidly changing developments in molecular pathology, pathologists are leading the way with new techniques such as fluorescence in-situ hybridization (FISH) and polymerase chain reaction (PCR), to map the genetic material in tissues or tumors, which are essential in the management of many cancers.</p>
                                <p class="mb-30">Many histopathologists specialize in specific organs such as the liver or skin, dissecting (‘cutting up’ or ‘trimming’) tissues for viewing under the microscope on a daily basis. For large specimens, such as samples of bowel or breast following surgery, these are dissected to select the most appropriate areas to examine under a microscope. Histopathologists write reports on specimens, consult literature (past and current research findings), and many also have teaching and research responsibilities. They will also attend multi-disciplinary meetings so their findings can be discussed with other clinicians. Treatments are then planned in detail and tailored to each individual patient.</p>
                                <p>Histopathologists also work directly with patients, for example, they may carry out procedures such as fine-needle aspiration in head and neck or breast clinics. They increasingly have key responsibilities for cancer screening, at the moment for breast, bowel, and cervical cancer, with other programs expected in the near future. Histopathologists also examine cells in smears, aspirates, or bodily fluids (cytopathology), for example in urine or cervical smears. Other subspecialties include forensic pathology, neuropathology, and paediatric pathology.</p>
